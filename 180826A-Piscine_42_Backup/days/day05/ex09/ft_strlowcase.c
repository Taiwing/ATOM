char	*ft_strlowcase(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr)
	{
		if (*ptr > 64 && *ptr < 91)
			*ptr = *ptr + 32;
		ptr++;
	}
	return (str);
}
