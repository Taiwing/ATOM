char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	n++;
	i = -1;
	while (src[++i] && --n)
		dest[i] = src[i];
	while (n && n--)
		dest[i++] = 0;
	return (dest);
}
