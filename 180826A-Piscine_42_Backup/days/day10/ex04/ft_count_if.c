int	ft_count_if(char **tab, int (*f)(char *))
{
	int	c;

	c = 0;
	if (!tab || !f)
		return (0);
	while (*tab)
	{
		if ((*f)(*tab++) == 1)
			c++;
	}
	return (c);
}
