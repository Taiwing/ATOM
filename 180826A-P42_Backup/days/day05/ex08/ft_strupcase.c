char	*ft_strupcase(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr)
	{
		if (*ptr > 96 && *ptr < 123)
			*ptr = *ptr - 32;
		ptr++;
	}
	return (str);
}
